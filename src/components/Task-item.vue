<template>
    <li >
        <button @click="$emit('complete')" :class="className">
            <i class="far fa-circle"></i> {{ task.title }}
        </button>
        <button @click="$emit('remove')"><i class="far fa-trash-alt"></i></button>
    </li>
</template>

<script lang="ts">
    export default{
        name: "taskItem",
        props:['task'],
        computed:{
            className() {
            let classes = ['toggle'];
            if (this.task.completed){
                classes.push('toggle-completed')
            }
            return classes;
            }
        },
    }
</script>